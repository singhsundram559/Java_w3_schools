public class Constant {
    public static void main(String[] args) {
        // "final" this variable declared a constant values which means unchangeable.
        final int MINUTE_PER_HOUR = 60;
        final int BIRTHDAY_YEAR = 2003;
        System.out.print("the minute per second is: ");
        System.out.println(MINUTE_PER_HOUR);
        System.out.print("the birthday year is: ");
        System.out.println(BIRTHDAY_YEAR);
    }
}
