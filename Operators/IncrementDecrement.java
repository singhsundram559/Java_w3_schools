public class IncrementDecrement {
    public static void main(String[] args) {
        int x = 6;
        ++x;  // increment x by 1
        System.out.println(x);


        int y = 10;
        --x;
        System.out.println(y); // decremnt y by 1
        int t = 23;
        ++t;  // incrent 23+1 = 24
        --t; // 24 -1 = 23 o/p = 23
        System.out.println(t);
    }
}
