public class printVariable {
    public static void main(String[] args) {
        String name = "john";
        System.out.println("hello " + name); // we can also + use add the another variable.

        String firstName = "sundram";
        String SecondName = "Singh";
        String FullName = firstName + SecondName;
        System.out.println(FullName); // this method is also callled a concatenation.

        int x = 4;
        int y = 67;
        System.out.println(x + y); // Similarly we can add integers value.
    }
}
