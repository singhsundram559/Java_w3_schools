public class CalculateAreaofRect {
    public static void main(String[] args) {
        int length = 23;
        int breadth = 21;
        int area;
        area = length * breadth;
        System.out.println("Length is: " + length);
        System.out.println("Width is: " + breadth);
        System.out.println("Area of the rectangle is: " + area);
    }
}
