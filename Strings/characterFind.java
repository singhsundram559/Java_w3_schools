public class characterFind {
    public static void main(String[] args) {
        String text = "Please where is 'locate' a character value";  // Finding a Character in a String
        System.out.println(text.indexOf("locate"));

        String some = "Hello World";
        System.out.println(some.charAt(2));  // finding a character value
        System.out.println(some.charAt(6));
    }
}
